namespace _03_MultipleVariables;

// Birden fazla değişkeni tek satırda başlatma
public static class Example2
{
    public static void Run()
    {
        Console.WriteLine("\n=== Example 2: Tek Satırda Başlatma ===");
        
        int x = 5, y = 10, z = 15;
        string ad = "Ali", soyad = "Veli";
        
        Console.WriteLine($"x={x}, y={y}, z={z}");
        Console.WriteLine($"Ad: {ad}, Soyad: {soyad}");
    }
}

