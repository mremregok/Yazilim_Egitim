namespace _01_Variables;

// Temel değişken tanımlama ve atama
public static class Example1
{
    public static void Run()
    {
        Console.WriteLine("=== Example 1: Temel Değişken Tanımlama ===");
        
        int sayi = 10;
        string isim = "Ahmet";
        double maas = 5000.50;
        
        Console.WriteLine($"Sayı: {sayi}");
        Console.WriteLine($"İsim: {isim}");
        Console.WriteLine($"Maaş: {maas}");
    }
}

